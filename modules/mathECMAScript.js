/*
    Módulo creado por mi para exportar funciones básicas de matemáticas
    usando las reglas de ECMAScript
*/

const add = (a,b) => {
    return a+b
}

const subtract = (a,b) => {
    return a-b
}

const multiply = (a,b) => {
    return a*b
}

export const divide = (a,b) => {
    return a/b
}

export default {add,subtract,multiply}